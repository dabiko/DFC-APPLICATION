@shared_task
def send_assignment_reminders():
    """Daily: Send reminder notifications for upcoming due dates."""
    from .models import ProcedureAssignment

    today = date.today()
    cadences = [7, 3, 1]  # Days before due

    for days_before in cadences:
        target_date = today + timedelta(days=days_before)
        assignments = ProcedureAssignment.objects.filter(
            due_date=target_date,
            status__in=['assigned', 'in_progress'],
        ).select_related('assignee', 'procedure_version')

        for assignment in assignments:
            # TODO: Send notification via notification system
            assignment.last_reminder_sent = timezone.now()
            assignment.reminder_count += 1
            assignment.save(update_fields=['last_reminder_sent', 'reminder_count'])

            logger.info(
                f"Reminder sent to {assignment.assignee} for "
                f"{assignment.procedure_version.title} (due in {days_before} days)"
            )


@shared_task
def check_overdue_assignments():
    """Daily: Mark overdue assignments and notify admins."""
    from .models import ProcedureAssignment

    updated = ProcedureAssignment.objects.filter(
        status__in=['assigned', 'in_progress'],
        due_date__lt=date.today(),
    ).update(status='overdue')

    logger.info(f"Marked {updated} assignments as overdue")
    return {'overdue_count': updated}


@shared_task
def check_expiring_versions():
    """Daily: Check for versions expiring within 30 days."""
    from .models import ProcedureVersion

    threshold = date.today() + timedelta(days=30)
    expiring = ProcedureVersion.objects.filter(
        is_active=True,
        expires_on__lte=threshold,
        expires_on__gte=date.today(),
    ).select_related('procedure')

    for version in expiring:
        # TODO: Send notification to admin
        logger.info(
            f"Version expiring soon: {version.title} v{version.version_number} "
            f"(expires {version.expires_on})"
        )

    return {'expiring_count': expiring.count()}


@shared_task
def auto_generate_recertifications():
    """Daily: Create re-certification assignments for expired versions."""
    from .models import ProcedureVersion, ProcedureAssignment

    expired = ProcedureVersion.objects.filter(
        is_active=True,
        expires_on__lt=date.today(),
    ).select_related('procedure')

    recert_count = 0
    for version in expired:
        # Check if a newer published version exists
        newer = ProcedureVersion.objects.filter(
            procedure=version.procedure,
            is_active=True,
            version_number__gt=version.version_number,
        ).first()

        if newer:
            # Find all users who completed the old version
            completed_users = ProcedureAssignment.objects.filter(
                procedure_version=version,
                status='completed',
            ).values_list('assignee_id', flat=True)

            # Skip already-assigned users
            already_assigned = ProcedureAssignment.objects.filter(
                procedure_version=newer,
                assignee_id__in=completed_users,
                status__in=['assigned', 'in_progress', 'completed'],
            ).values_list('assignee_id', flat=True)

            to_assign = set(completed_users) - set(already_assigned)

            for user_id in to_assign:
                ProcedureAssignment.objects.create(
                    organization=version.procedure.organization,
                    procedure_version=newer,
                    assignee_id=user_id,
                    assigned_by_id=version.published_by_id,
                    assignment_source='recertification',
                    due_date=date.today() + timedelta(days=30),
                )
                recert_count += 1

    logger.info(f"Created {recert_count} re-certification assignments")
    return {'recert_count': recert_count}


@shared_task
def cleanup_abandoned_attempts():
    """Weekly: Mark stale in-progress attempts as abandoned (>30 days inactive)."""
    from .models import TrainingAttempt

    threshold = timezone.now() - timedelta(days=30)
    updated = TrainingAttempt.objects.filter(
        status='in_progress',
        started_at__lt=threshold,
    ).update(status='abandoned')

    logger.info(f"Marked {updated} training attempts as abandoned")
    return {'abandoned_count': updated}
