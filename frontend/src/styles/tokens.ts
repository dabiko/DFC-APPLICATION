// Type-safe helper to get color values
export type ColorShade = 50 | 100 | 200 | 300 | 400 | 500 | 600 | 700 | 800 | 900 | 950
export type ColorPalette = keyof typeof tokens.colors
export type ConfidentialityLevel = keyof typeof tokens.colors.confidentiality

// Helper functions for accessing tokens
export const getColor = (palette: ColorPalette, shade?: ColorShade): string => {
  if (palette === 'confidentiality') {
    return tokens.colors.confidentiality.public
  }
  const paletteColors = tokens.colors[palette] as Record<string, string>
  return shade ? paletteColors[shade] : paletteColors['500']
}

export const getConfidentialityColor = (level: ConfidentialityLevel): string => {
  return tokens.colors.confidentiality[level]
}
